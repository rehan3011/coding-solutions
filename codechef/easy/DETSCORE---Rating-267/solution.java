import java.util.*;
import java.lang.*;
import java.io.*;

class Codechef
{
	public static void main (String[] args) throws java.lang.Exception
	{
	        Scanner sc = new Scanner(System.in);
	        
	        int n = sc.nextInt();
	        int x,y,z;
	        for(int i=0; i<n; i++){
	             x = sc.nextInt();
	             y= sc.nextInt();
	             z = (x*y)/10;
	            System.out.println(z);
	        }

	}
}

import java.util.*;
import java.lang.*;
import java.io.*;

class Codechef
{
	public static void main (String[] args) throws java.lang.Exception
	{
		Scanner sc = new Scanner(System.in);
		
		int n = sc.nextInt();
		int cl, pr;
		
		for(int i=0; i<n; i++){
		 cl = sc.nextInt();
		 pr = sc.nextInt();
		 
		 System.out.println(cl*pr);
		}

	}
}
